
#Time Left Calculator

cage = int( input("What is your current age?\n"))

lyears = 90 - cage 
lmonths = lyears * 12
lweeks = lyears * 53
ldays = lyears * 365

print (f"If you live till the age of 90 then from current you are left with {lyears} years, \n{lmonths} months,{lweeks} weeks and {ldays} days!")
